package model;

public class Excel {

	public double soma(double valores[]) {
		double total = 0;
		for (int i = 0; i < valores.length; i++) {
			total = valores[i];
		}
		return total;
	}
	public double sub(double valores[]) {
		double total = 0;
		for (int i = 0; i < valores.length; i++) {
			total = valores[i];
		}
		return total;
	}
	public double mult(double valores[]) {
		double total = 0;
		for (int i = 0; i < valores.length; i++) {
			total = valores[i];
		}
		return total;
	}
	public double div(double valores[]) {
		double total = 0;
		for (int i = 0; i < valores.length; i++) {
			total = valores[i];
		}
		return total;
	}

}
