public class Variaveis{
	public static void main(String[] args){
	
		//exemplo de tipo byte
		byte byte1 = 65;
		byte byte2 = 66;
		int soma = byte1 + byte2;
		System.out.println(soma);
		
		//exemplo de tipo char
		char char1 = 65;
		char char2 = 66;
		String soma2 =  char1 + " " + char2;
		System.out.println(soma2);
		
		System.out.println("A soma de dois bytes é:"+ soma);
		System.out.println("A soma de dois chares é:"+ soma2);
		
	}
}